export type TranslationValues = Readonly<Record<string, string | number>>;

export function translateUi(
  language: UiLanguage,
  source: string,
  values: TranslationValues = {},
): string {
  const template = language === "ja" ? JA[source] ?? source : source;
  return template.replace(/\{([A-Za-z0-9_]+)\}/g, (match, key: string) =>
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match);
}

/** Used by localization coverage tests to distinguish a deliberate translation from fallback. */
export function hasUiTranslation(language: UiLanguage, source: string): boolean {
  return language === "en" || Object.prototype.hasOwnProperty.call(JA, source);
}

interface I18nContextValue {
  readonly language: UiLanguage;
  readonly setLanguage: (language: UiLanguage) => void;
  readonly t: (source: string, values?: TranslationValues) => string;
}

const DEFAULT_CONTEXT: I18nContextValue = {
  language: "en",
  setLanguage: () => undefined,
  t: (source, values) => translateUi("en", source, values),
};

const I18nContext = createContext<I18nContextValue>(DEFAULT_CONTEXT);

function storedLanguage(): UiLanguage {
  try {
    return window.localStorage.getItem(STORAGE_KEY) === "ja" ? "ja" : "en";
  } catch {
    return "en";
  }
}

export function I18nProvider({ children }: Readonly<{ children: ReactNode }>) {
  const [language, setLanguageState] = useState<UiLanguage>(storedLanguage);
  const setLanguage = (next: UiLanguage) => {
    setLanguageState(next);
    try {
      window.localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // Private browsing or storage policy may reject persistence; the live choice still works.
    }
  };
  useEffect(() => {
    document.documentElement.lang = language === "ja" ? "ja" : "en";
  }, [language]);
  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    t: (source, values) => translateUi(language, source, values),
  }), [language]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
